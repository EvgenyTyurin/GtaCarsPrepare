public class GtaCar {
    private String name;
    private String wikiUrl;

    public GtaCar(String name, String wikiUrl) {
        this.name = name;
        this.wikiUrl = wikiUrl;
    }

    public String getName() {
        return name;
    }

    public String getWikiUrl() {
        return wikiUrl;
    }
}
